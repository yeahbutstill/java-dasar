package com.yeahbutstill.javadasar.operasi.matematika;

public class OperasiMatematika {

    public static void main(String[] args) {

        int a = 100;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println("====================");

        // Augmented Assignments
        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        // Unary Operator


    }

}
